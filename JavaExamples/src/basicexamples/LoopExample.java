package basicexamples;

public class LoopExample {
    public static void main(String[] args) {


        for (int i = 1; i <= 10; i+=2) {  //vypíše nepárne čísla.
            System.out.println(i);

        }

        System.out.println();

        for (int i = 1; i <= 10; i*=2) {  //vypíše číslo 1, 2, 3, 4,  8 ako nasobky 2-och.
            System.out.println(i);

        }



}
}
