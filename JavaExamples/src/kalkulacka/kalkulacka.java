package kalkulacka;

public class kalkulacka {

    public static void main(String[] args) {

        int number1 = 3;
        int number2 = 5;

        System.out.println("Výsledok sčítanie čísel  3  a  5 je = " + (number1 + number2));
        System.out.println("Výsledok odčítanie čísel  3  a  5 je = " + (number1 - number2));
        System.out.println("Výsledok kratenia čísel  3  a  5 je = " + number1 * number2);
        System.out.println("Výsledok delenia čísel  3  a  5 je = " + number1 / number2);


    }

}
